// ─── Predictions ──────────────────────────────────────────────────────────────
export function useItemPrediction(itemId: string) {
  return useQuery({
    queryKey: ["prediction", itemId],
    queryFn: () => predictionsApi.item(itemId).then((r) => r.data),
    enabled: !!itemId,
    staleTime: 5 * 60_000,
  });
}

export function useBulkPredictions() {
  return useQuery({
    queryKey: ["bulk-predictions"],
    queryFn: () => predictionsApi.bulk().then((r) => r.data),
    staleTime: 5 * 60_000,
  });
}

