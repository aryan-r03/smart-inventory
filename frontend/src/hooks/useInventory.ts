export function useAutoSuggest() {
  const qc = useQueryClient();
  return useMutation({
    mutationFn: () => procurementApi.autoSuggest(),
    onSuccess: (res) => {
      qc.invalidateQueries({ queryKey: ["procurement"] });
      toast.success(`${res.data.created} suggestions generated by AI`);
    },
    onError: () => toast.error("Auto-suggest failed"),
  });
}
